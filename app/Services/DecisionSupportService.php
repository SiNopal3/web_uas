    /**
     * Helper to get Decision Score badge label.
     */
    protected function getDecisionScoreBadge(float $score): string
    {
        if ($score >= 70) return 'IMMEDIATE ACTION REQUIRED';
        if ($score >= 50) return 'HIGH INTERVENTION PRIORITY';
        if ($score >= 30) return 'MODERATE ADVISORY';
        return 'OPTIMAL PASSIVE MONITORING';
    }

    /**
     * Calculate average of a specific key across array.
     */
    protected function calculateAverage(array $items, string $key): float
    {
        if (empty($items)) return 0.0;
        $sum = array_sum(array_column($items, $key));
        return round($sum / count($items), 1);
    }

    /**
     * Clamp float between 0 and 100.
     */
    protected function clamp(float $val): float
    {
        return round(max(0, min(100, $val)), 1);
    }
