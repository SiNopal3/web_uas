        $priorityOrder = ['CRITICAL' => 1, 'HIGH' => 2, 'MEDIUM' => 3, 'LOW' => 4];
        usort($triggeredActions, function ($a, $b) use ($priorityOrder) {
            return ($priorityOrder[$a['priority']] ?? 5) <=> ($priorityOrder[$b['priority']] ?? 5);
        });

        // Take top 12 most critical distinct actions for dashboard display
        return [
            'actions' => array_slice($triggeredActions, 0, 12),
            'triggered_ids' => $triggeredIds
        ];
