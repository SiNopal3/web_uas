    /**
     * Calculate Financial Impact (Delay Cost, Insurance, Inventory, Revenue Loss, Total Exposure).
     */
    public function calculateFinancialImpact(array $countries, array $triggeredIds): array
    {
        // Base economic volume assumption per corridor ($500,000 daily transit baseline)
        $delayCost = 0;
        $insuranceCost = 0;
        $inventoryCost = 0;
        $revenueRisk = 0;
        $currencyLoss = 0;

        foreach ($countries as $c) {
            $fRisk = (float) $c['future_7d_risk'];
            $dScore = (float) $c['delay_probability_score'];
            $wRisk = (float) $c['weather_risk'];
            $iRisk = (float) $c['inflation_risk'];
            $curRisk = (float) $c['currency_risk'];
            $pRisk = (float) $c['port_risk'];

            // Estimated delay days
            $days = 1;
            if ($dScore >= 65) $days = 7;
            elseif ($dScore >= 50) $days = 5;
            elseif ($dScore >= 35) $days = 3;

            // Dollar impact formulas
            $delayCost += ($days * 85000) * ($fRisk / 50);
