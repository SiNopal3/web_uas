    /**
     * Get complete structured data for AI Decision Support Center (`/decision-support`).
     *
     * @param array $simulatedDeltas Optional deltas for Scenario Simulator (weather_delta, inflation_delta, currency_delta, news_delta, delay_delta, prediction_delta)
     * @return array Complete DSS data structure
     */
    public function getDecisionSupportData(array $simulatedDeltas = []): array
    {
        // 1. Fetch baseline and predicted countries data via PredictionService without code duplication
        // Extract basic prediction deltas and pass to PredictionService
        $predDeltas = [
            'weather_delta' => (float) ($simulatedDeltas['weather_delta'] ?? 0),
            'inflation_delta' => (float) ($simulatedDeltas['inflation_delta'] ?? 0),
            'currency_delta' => (float) ($simulatedDeltas['currency_delta'] ?? 0),
            'news_delta' => (float) ($simulatedDeltas['news_delta'] ?? 0),
        ];
        $predictionData = $this->predictionService->getPredictionData($predDeltas);
        $countries = $predictionData['ranking_table'] ?? [];
        
        // Re-enrich country details from prediction service collection for full indicator access
        $detailedCountries = $this->predictionService->collectCountriesData($predDeltas);

        // Apply extra delay and prediction simulation deltas if present
        $delayDelta = (float) ($simulatedDeltas['delay_delta'] ?? 0);
        $predictionDelta = (float) ($simulatedDeltas['prediction_delta'] ?? 0);
        if ($delayDelta != 0 || $predictionDelta != 0) {
            foreach ($detailedCountries as &$c) {
                $c['future_7d_risk'] = $this->clamp($c['future_7d_risk'] + $predictionDelta);
                $c['delay_probability_score'] = $this->clamp($c['delay_probability_score'] + $delayDelta);
                $c['shipping_delay'] = $this->predictionService->classifyShippingDelay($c['delay_probability_score']);
            }
        }

        // 2. Evaluate 40+ Expert Rules & generate Recommended Actions
        $ruleEvaluation = $this->evaluateExpertRules($detailedCountries, $simulatedDeltas);
        $recommendedActions = $ruleEvaluation['actions'];
        $triggeredRuleIds = $ruleEvaluation['triggered_ids'];

        // 3. Financial Impact Analysis (Rule-based financial exposure model)
        $financialModel = $this->calculateFinancialImpact($detailedCountries, $triggeredRuleIds);

        // 4. Decision Score & Emergency Dashboard Level
        $decisionScore = $this->calculateDecisionScore($detailedCountries, $financialModel['total_exposure']);
        $emergencyLevel = $this->classifyEmergencyLevel($decisionScore, $detailedCountries);

        // 5. Decision Matrix (Risk vs Business Impact across 4 Quadrants)
        $decisionMatrix = $this->generateDecisionMatrix($detailedCountries);

        // 6. Alternative Route & Port Recommendations
        $altRoutes = $this->generateAlternativeRoutes($detailedCountries);

        // 7. Alternative Supplier Recommendations
        $altSuppliers = $this->generateAlternativeSuppliers($detailedCountries);

        // 8. Executive Action Timeline (Immediate to Next Week structured by Department)
        $actionTimeline = $this->generateActionTimeline($recommendedActions, $detailedCountries);

        // 9. Strategy Comparison (Current Strategy vs Recommended Rule-Based Strategy)
        $strategyComparison = $this->generateStrategyComparison($detailedCountries, $decisionScore, $financialModel);

        // 10. Executive Summary Narrative (Generated by PHP Rule Engine)
        $executiveSummary = $this->generateExecutiveSummary($detailedCountries, $emergencyLevel, $recommendedActions, $altRoutes);

        // 11. Chart Datasets (Matrix, Financials, Radar, Doughnut Gauge, Exposure Bar)
        $charts = $this->generateChartDatasets($decisionMatrix, $financialModel, $strategyComparison, $decisionScore);

        return [
            'header' => [
                'title' => 'AI Decision Support Center',
                'subtitle' => 'Enterprise Supply Chain Decision System',
                'timestamp' => Carbon::now()->setTimezone('Asia/Jakarta')->format('d M Y, h:i:s A'),
                'status' => 'ONLINE (PHP Rule Engine v2.4 Active)'
            ],
            'kpi_cards' => [
                'decision_score' => $decisionScore,
                'decision_score_badge' => $this->getDecisionScoreBadge($decisionScore),
                'emergency_level' => $emergencyLevel['level'],
                'emergency_color' => $emergencyLevel['color'],
                'business_impact' => round($this->calculateAverage($decisionMatrix, 'impact_score'), 1) . '%',
                'financial_exposure' => '$' . number_format($financialModel['total_exposure'] / 1000, 1) . 'M',
                'estimated_delay' => $this->getHighestDelayDays($detailedCountries),
                'recommended_action' => $recommendedActions[0]['action'] ?? 'Maintain Standard Protocol',
                'supply_chain_stability' => round(100 - $this->calculateAverage($detailedCountries, 'future_7d_risk'), 1) . '%',
                'operational_status' => $emergencyLevel['operational_status']
            ],
            'decision_matrix' => $decisionMatrix,
            'recommended_actions' => $recommendedActions,
            'financial_impact' => $financialModel,
            'alternative_routes' => $altRoutes,
            'alternative_suppliers' => $altSuppliers,
            'action_timeline' => $actionTimeline,
            'emergency_dashboard' => $emergencyLevel,
            'strategy_comparison' => $strategyComparison,
            'executive_summary' => $executiveSummary,
            'charts' => $charts,
            'comparison_engine' => $this->generateComparisonEngineData($detailedCountries),
            'rules_catalog_count' => count($this->getExpertRulesCatalog()),
            'rules_triggered_count' => count($triggeredRuleIds),
            'simulation_applied' => !empty($simulatedDeltas) && (
                ($simulatedDeltas['weather_delta'] ?? 0) != 0 ||
                ($simulatedDeltas['inflation_delta'] ?? 0) != 0 ||
                ($simulatedDeltas['currency_delta'] ?? 0) != 0 ||
                ($simulatedDeltas['news_delta'] ?? 0) != 0 ||
                ($simulatedDeltas['delay_delta'] ?? 0) != 0 ||
                ($simulatedDeltas['prediction_delta'] ?? 0) != 0
            )
        ];
    }

