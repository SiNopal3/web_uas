    /**
     * Calculate composite Decision Score (0 - 100).
     */
    public function calculateDecisionScore(array $countries, float $totalExposure): float
    {
        $avgPrediction = $this->calculateAverage($countries, 'future_7d_risk');
        $avgDelay = $this->calculateAverage($countries, 'delay_probability_score');
        $avgWeather = $this->calculateAverage($countries, 'weather_risk');
        $avgNews = $this->calculateAverage($countries, 'news_risk');
        $avgInflation = $this->calculateAverage($countries, 'inflation_risk');
        $avgCurrency = $this->calculateAverage($countries, 'currency_risk');

        // Financial exposure scaling factor ($5M exposure = 100%)
        $exposureScore = min(100, ($totalExposure / 5000000) * 100);

        // Weighted formula
        $composite = ($avgPrediction * 0.30) +
                     ($avgDelay * 0.25) +
                     ($exposureScore * 0.15) +
                     ($avgWeather * 0.12) +
                     ($avgNews * 0.10) +
                     (($avgInflation + $avgCurrency) * 0.04);

        return round($this->clamp($composite), 1);
    }

